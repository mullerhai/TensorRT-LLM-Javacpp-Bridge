def opaque_class(pkg, ns, classname, doc=""):
    """Generate an @Opaque class (simple pointer wrapper)"""
    docstr = f"\n/** {doc} */\n" if doc else "\n"
    return STANDARD_IMPORTS.format(pkg=pkg) + f'''{docstr}@Namespace("{ns}") @Opaque @Properties(inherit = tensorrt_llm.presets.TRTLLMFullConfig.class)
public class {classname} extends Pointer {{
    /** Empty constructor. Calls {{@code super((Pointer)null)}}. */
    public {classname}() {{ super((Pointer)null); }}
    /** Pointer cast constructor. Invokes {{@link Pointer#Pointer(Pointer)}}. */
    public {classname}(Pointer p) {{ super(p); }}
}}
'''

def full_class(pkg, ns, classname, body, doc=""):
    """Generate a full class with methods"""
    docstr = f"\n/** {doc} */\n" if doc else "\n"
    return STANDARD_IMPORTS.format(pkg=pkg) + f'''{docstr}@Namespace("{ns}") @NoOffset @Properties(inherit = tensorrt_llm.presets.TRTLLMFullConfig.class)
public class {classname} extends Pointer {{
    static {{ Loader.load(); }}
    /** Pointer cast constructor. Invokes {{@link Pointer#Pointer(Pointer)}}. */
    public {classname}(Pointer p) {{ super(p); }}

{body}}}
'''

def enum_class(pkg, ns, classname, values, doc=""):
    """Generate an enum"""
    docstr = f"\n/** {doc} */\n" if doc else "\n"
    vals = ""
    for i, (name, val) in enumerate(values):
        comma = "," if i < len(values) - 1 else ";"
        vals += f"    {name}({val}){comma}\n"

    return STANDARD_IMPORTS.format(pkg=pkg) + f'''{docstr}@Namespace("{ns}") public enum {classname} {{
{vals}
    public final int value;
    private {classname}(int v) {{ this.value = v; }}
    private {classname}({classname} e) {{ this.value = e.value; }}
    public {classname} intern() {{ for ({classname} e : values()) if (e.value == value) return e; return this; }}
    @Override public String toString() {{ return intern().name(); }}
}}
'''

def write_file(subpath, content):
    """Write a file under BASE_DIR"""
    path = os.path.join(BASE_DIR, subpath)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, 'w') as f:
        f.write(content)
    print(f"  Created: {subpath}")


