# Generate all missing Java binding files for TensorRT-LLM
# Target: /Users/mullerzhang/IdeaProjects/TRTLLM-Java-Bridge/src/main/java/org/bytedeco/tensorrt_llm/

BASE="/Users/mullerzhang/IdeaProjects/TRTLLM-Java-Bridge/src/main/java/org/bytedeco/tensorrt_llm"

gen_opaque() {
    local pkg="$1" ns="$2" cls="$3" doc="$4"
    local dir="$BASE/$pkg"
    local file="$dir/$cls.java"

    if [ -f "$file" ]; then
        echo "EXISTS: $pkg/$cls.java"
        return
    fi

    mkdir -p "$dir"

    local javapkg
    javapkg=$(echo "$pkg" | tr '/' '.')

    cat > "$file" << JAVAEOF
// Targeted by JavaCPP version 1.5.13: DO NOT EDIT THIS FILE
package org.bytedeco.tensorrt_llm.${javapkg};
import org.bytedeco.tensorrt_llm.*;import org.bytedeco.tensorrt_llm.executor.*;import org.bytedeco.tensorrt_llm.executor.kvcache.*;import org.bytedeco.tensorrt_llm.runtime.*;import org.bytedeco.tensorrt_llm.batch_manager.*;import org.bytedeco.tensorrt_llm.batch_manager.kvcache.*;import org.bytedeco.tensorrt_llm.common.*;import org.bytedeco.tensorrt_llm.kernels.*;import org.bytedeco.tensorrt_llm.layers.*;import org.bytedeco.tensorrt_llm.plugins.*;import org.bytedeco.tensorrt_llm.mpi.*;import org.bytedeco.tensorrt_llm.nvinfer.*;
import java.nio.*;import org.bytedeco.javacpp.*;import org.bytedeco.javacpp.annotation.*;
import static org.bytedeco.tensorrt_llm.global.TRTLLM.*;
/** ${doc} */ @Namespace("${ns}") @Opaque @Properties(inherit = tensorrt_llm.presets.TRTLLMFullConfig.class) public class ${cls} extends Pointer { public ${cls}() { super((Pointer)null); } public ${cls}(Pointer p) { super(p); } }
JAVAEOF
    echo "NEW: $pkg/$cls.java"
}

